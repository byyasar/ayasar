bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case SHUT: // oturum kapatır
            if (record->event.pressed) {
                register_code(KC_LGUI);
                register_code(TR_R);
                unregister_code(TR_R);
                unregister_code(KC_LGUI);
                tap_code_delay(KC_WH_U, 1000);
                SEND_STRING(" logoff");
                tap_code(KC_ENT);
            }
            break;

        case MYCHANGELAYER:
            if (record->event.pressed) {
                layer_off(myLayer);
                myLayer++;
                myLayer=myLayer % 2;
                layer_on(myLayer);
               
            }
            break;
    }
    return true;
};

